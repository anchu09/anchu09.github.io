export function iconClass(url: string): string {
  if (url.startsWith('/icons/')) return 'w-3.5 h-3.5 rounded'
  if (url.includes('heroicons') || url.includes('phosphor') || url.includes('lucide')) return 'w-3 h-3 brightness-0 invert opacity-50'
  return 'w-3.5 h-3.5'
}
